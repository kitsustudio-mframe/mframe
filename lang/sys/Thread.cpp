//-----------------------------------------------------------------------------
bool Thread::start(const char* name) {
  return this->start(name, mframe::lang::sys::ThreadPriority::NORMAL);
}

//-----------------------------------------------------------------------------
bool Thread::isActive(void) {
  mframe::lang::sys::ThreadState state = this->getState();

  switch (state) {
    case mframe::lang::sys::ThreadState::INACTIVE:
    case mframe::lang::sys::ThreadState::ERROR:
      return false;

    case mframe::lang::sys::ThreadState::BLOCKED:
    case mframe::lang::sys::ThreadState::READY:
    case mframe::lang::sys::ThreadState::RUNNING:
    case mframe::lang::sys::ThreadState::TERMINATED:
      return true;
  }
}

